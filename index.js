function newImage(url, left, bottom){
    let pics = document.createElement('img')
    pics.src = url
    pics.style.position = 'fixed'
    pics.style.left = left + 'px'
    pics.style.bottom = bottom + 'px'
    document.body.append(pics)
    return pics
}

